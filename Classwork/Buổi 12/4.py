#Ngăn xếp và hàng đợi (stack & queue)

#Stack: stack là một cấu trúc dữ liệu tuân theo nguyên tắc last in, first out (LIFO), phần tử cuối cùng được thêm vào sẽ là phần tử đầu tiên được loại bỏ.
#Ví dụ của stack sẽ là từng viên đạn trong băng đạn, viên đạn cuối cùng sẽ là viên đạn đầu tiên rời khỏi súng.

#Queue: queue là một cấu trúc dữ liệu tuân theo nguyên tắc first in, frst out (FIFO), phần tử đầu tiên được thêm vào sẽ là phần tử đầu tiên được loại bỏ.
#Ví dụ của queue sẽ là gọi grab, người đặt trước sẽ có chuyến trước.